package arcane_arcade_main;

/**
 * Buttons are the main buttons used in the game to control the wizards. Buttons 
 * can be mapped to different keys.
 *
 * @author Mikko Hilpinen.
 *         Created 1.9.2013.
 */
public enum Buttons
{
	/**
	 * The button used to move up
	 */
	UP, 
	/**
	 * The button used to move down
	 */
	DOWN,
	/**
	 * The button used to change the left element up
	 */
	LEFT_ELEMENT_UP,
	/**
	 * The button used to change the left element down
	 */
	LEFT_ELEMENT_DOWN, 
	/**
	 * The button used to change the right element up
	 */
	RIGHT_ELEMENT_UP, 
	/**
	 * The button used to change the right element down
	 */
	RIGHT_ELEMENT_DOWN,
	/**
	 * The button used to cast a spell
	 */
	CAST;
}
